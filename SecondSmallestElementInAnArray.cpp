#include <iostream>
using namespace std;
int main()
{
    int arr[5] = {9, 1, 5, 3, 7};
    int min1, min2;
    min1 = arr[0];
    min2 = arr[0];
    for(int i =  0; i < 5; i++)
    {
        if(arr[i] < min1)
        min1 = arr[i];
        else if(arr[i] < min2)
        min2 = arr[i];
    }
    cout << "Second smallest element in an array is : " << min2 << endl;
    return 0;
}


