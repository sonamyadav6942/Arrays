#include <iostream>
using namespace std;
int main()
{
    int arr[7] = {1, 7, 8, 4, 5, 16, 8};
    int even_count = 0, odd_count = 0;
    for(int i = 0; i < 7; i++)
    {
        if(arr[i] % 2 == 0)
        even_count++;
        else
        odd_count++;
    }
    cout << "Even elements count : " << even_count << "\nOdd Elements count : " << odd_count;
    return 0;
}


